/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode* root) {
        if (!root) return {};
        vector<vector<int>> res;
        queue<TreeNode*> q;
        q.push(root);
        while(!q.empty())
        {
            int size = q.size();
            vector<int> temp;
            while (size)
            {
                TreeNode* nodeTemp = q.front();
                q.pop();
                temp.push_back(nodeTemp->val);
                if (nodeTemp->left) q.push(nodeTemp->left);
                if (nodeTemp->right) q.push(nodeTemp->right);
                size--;
            }
            res.push_back(temp);
        }
        return res;
    }
};
